
public class Soldier {
	
	private int x;
	private int y;
	private String team;
	private boolean alive;
	private String name;
	
	public Soldier(int x, int y, String team, boolean alive, String name) {
		this.x = x;
		this.y = y;
		this.team = team;
		this.alive = alive;
		this.name = name;
	}

}
